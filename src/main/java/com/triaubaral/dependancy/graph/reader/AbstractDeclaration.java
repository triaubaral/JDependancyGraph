package com.triaubaral.dependancy.graph.reader;

public abstract class AbstractDeclaration {
	
	private String value;

	public AbstractDeclaration(String value) {
		super();
		this.value = value;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
	
	public String getJavaInstruction(){
		return getJavaKeyWord()+" "+getValue()+";";
	}
	
	public abstract String getJavaKeyWord();

	@Override
	public String toString() {
		return "AbstractDeclaration [value=" + value + "]";
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((value == null) ? 0 : value.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		AbstractDeclaration other = (AbstractDeclaration) obj;
		if (value == null) {
			if (other.value != null)
				return false;
		} else if (!value.equals(other.value))
			return false;
		return true;
	}
	
}
